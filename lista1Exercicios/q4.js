//Crie uma função que irá receber dois valores, o dividendo e o divisor. 
// A função deverá imprimir o resultado
// e o resto da divisão destes dois valores.

const divisao = function (dividendo, divisor){
    console.log(`${dividendo}/${divisor} = ${dividendo/divisor} : % = ${dividendo%divisor}`)
}

divisao(9,5)